#!/usr/bin/env bash

#=================================================================
# Message Color Codes
#=================================================================
BLUE='\033[0;34m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
RED='\033[0;31m'
RESET='\033[0m'

#=================================================================
# Arrow symbols
#=================================================================
UP_ARROW=$'\u25B2'
DOWN_ARROW=$'\u25BC'

#=================================================================
# Message functions
#=================================================================
info() {
  printf "%b\n" "${BLUE}INFO: $*${RESET}"
}

success() {
  printf "%b\n" "${GREEN}SUCCESS: $*${RESET}"
}

warn() {
  printf "%b\n" "${YELLOW}WARNING: $*${RESET}"
}

error() {
  printf "%b\n" "${RED}ERROR: $*${RESET}"
}

line_blue() {
  printf "%b\n" "${BLUE}**********************************************************************${RESET}"
}

line_red() {
  printf "%b\n" "${RED}======================================================================${RESET}"
}

info_box() {
  printf "%b\n" "${BLUE}${DOWN_ARROW}********************************************************************${DOWN_ARROW}${RESET}"
  info "$*"
  printf "%b\n" "${BLUE}${UP_ARROW}********************************************************************${UP_ARROW}${RESET}"
}
